class IDMapper:
    """
    Класс для двустороннего маппинга идентификаторов (ID) во внутренние индексы.

    Используется для преобразования произвольных ID (пользователей или товаров) 
    в последовательные целые числа от 0 до N-1, что необходимо для работы 
    с разреженными матрицами и моделями машинного обучения.

    Attributes:
        unique_ids (list): Отсортированный список уникальных идентификаторов.
        id_to_idx (dict): Словарь соответствия внешнего ID внутреннему индексу.
        idx_to_id (dict): Словарь соответствия внутреннего индекса внешнему ID.
    """
    def __init__(self, ids):
        """
        Инициализирует маппер и строит таблицы соответствия.

        Args:
            ids (iterable): Коллекция исходных идентификаторов 
        """
        self.unique_ids = sorted(set(ids))
        self.id_to_idx = {id_: i for i, id_ in enumerate(self.unique_ids)}
        self.idx_to_id = {i: id_ for i, id_ in enumerate(self.unique_ids)}

    def to_idx(self, ids):
        """
        Преобразует набор внешних ID в соответствующие внутренние индексы.

        Args:
            ids (pd.Series): Серия идентификаторов для преобразования.

        Returns:
            pd.Series: Серия индексов. Значения, не найденные в маппере, 
                будут заменены на NaN (по правилам метода .map()).
        """
        return ids.map(self.id_to_idx)

    @property
    def size(self):
        """
        Возвращает количество уникальных идентификаторов в маппере.

        Returns:
            int: Размер словаря (количество строк/столбцов в будущей матрице).
        """
        return len(self.unique_ids)